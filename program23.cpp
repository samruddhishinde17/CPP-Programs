//Using "WHILE LOOOP" Concept
//Display "Jay Hanuman" on screen 5 times

#include<iostream>
using namespace std;

void Display()
{
    int iCnt = 1;

    while (iCnt <= 5)
    {
        cout<<"Jay HAnuman..."<<endl;

        iCnt++;
    }
}

int main()
{
    Display();

    return 0;
}