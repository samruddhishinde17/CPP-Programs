//Using "FOR LOOOP" Concept
//Display "Jay Hanuman" on screen 5 times

#include<iostream>
using namespace std;

void Display()
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
        cout<<"Jay HAnuman..."<<endl;
    }
}

int main()
{
    Display();

    return 0;
}